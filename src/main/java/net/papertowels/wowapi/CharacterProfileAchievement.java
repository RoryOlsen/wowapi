import java.util.List;

public class CharacterProfileAchievement {

	private List achievementsCompleted;
	private List achievementsCompletedTimestamp;
	private List criteria;
	private List criteriaCreated;
	private List criteriaQuanity;
	private List criteriaTimestamp;
	
	public List getAchievementsCompleted() {
		return achievementsCompleted;
	}
	public void setAchievementsCompleted(List achievementsCompleted) {
		this.achievementsCompleted = achievementsCompleted;
	}
	public List getAchievementsCompletedTimestamp() {
		return achievementsCompletedTimestamp;
	}
	public void setAchievementsCompletedTimestamp(List achievementsCompletedTimestamp) {
		this.achievementsCompletedTimestamp = achievementsCompletedTimestamp;
	}
	public List getCriteria() {
		return criteria;
	}
	public void setCriteria(List criteria) {
		this.criteria = criteria;
	}
	public List getCriteriaCreated() {
		return criteriaCreated;
	}
	public void setCriteriaCreated(List criteriaCreated) {
		this.criteriaCreated = criteriaCreated;
	}
	public List getCriteriaQuanity() {
		return criteriaQuanity;
	}
	public void setCriteriaQuanity(List criteriaQuanity) {
		this.criteriaQuanity = criteriaQuanity;
	}
	public List getCriteriaTimestamp() {
		return criteriaTimestamp;
	}
	public void setCriteriaTimestamp(List criteriaTimestamp) {
		this.criteriaTimestamp = criteriaTimestamp;
	}
	
	
}
