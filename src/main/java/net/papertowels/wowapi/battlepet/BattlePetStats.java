package net.papertowels.wowapi.battlepet;

public class BattlePetStats {
	
	private long breedId;
	private int health;
	private int level;
	private long petQualityId;
	private int power;
	private long speciesId;
	private int speed;
	
	public long getBreedId() {
		return breedId;
	}
	public void setBreedId(long breedId) {
		this.breedId = breedId;
	}
	public int getHealth() {
		return health;
	}
	public void setHealth(int health) {
		this.health = health;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public long getPetQualityId() {
		return petQualityId;
	}
	public void setPetQualityId(long petQualityId) {
		this.petQualityId = petQualityId;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public long getSpeciesId() {
		return speciesId;
	}
	public void setSpeciesId(long speciesId) {
		this.speciesId = speciesId;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
}
